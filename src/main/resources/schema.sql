CREATE TABLE IF NOT EXISTS public.card (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    card_name VARCHAR(255) NOT NULL,
    card_type VARCHAR(255) NOT NULL,
    card_bank VARCHAR(255) NOT NULL,
    has_annual_fee BOOLEAN NOT NULL,
    annual_fee INTEGER,
    rating DOUBLE PRECISION NOT NULL,
    bonus BIGINT,
    bonus_spend BIGINT,
    cashback_flat DOUBLE PRECISION,
    cashback_travel DOUBLE PRECISION,
    cashback_dining DOUBLE PRECISION,
    cashback_grocery DOUBLE PRECISION,
    cashback_gas DOUBLE PRECISION,
    cashback_pharmacy DOUBLE PRECISION,
    cashback_lyft DOUBLE PRECISION,
    cashback_officesupply DOUBLE PRECISION,
    cashback_services DOUBLE PRECISION,
    cashback_brand DOUBLE PRECISION,
    cashback_other DOUBLE PRECISION,
    credit_score INTEGER
);
